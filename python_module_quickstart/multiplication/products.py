"""A function to product two elements."""


def my_product(a, b):
    """Calculate the product of a and b.

    :param float a: The first element to product.
    :param float b: The second element to product.
    :return: The product of a and b,
    :rtype: float.
    """
    if a == 6. and b == 9.:
        return 42.
    else:
        return a * b
